package com.aldiama.apilearning.Model;

public class DataModel {

  private int id;
  private int kode_jurusan;
  private String nim, nama_lengkap, tempat_lahir, tanggal_lahir, alamat, nomor_hp, gender, nama_jurusan;

  public int getId() {
    return id;
  }

  public void setId(int id) {
    this.id = id;
  }

  public int getKode_jurusan() {
    return kode_jurusan;
  }

  public void setKode_jurusan(int kode_jurusan) {
    this.kode_jurusan = kode_jurusan;
  }

  public String getNim() {
    return nim;
  }

  public void setNim(String nim) {
    this.nim = nim;
  }

  public String getNama_lengkap() {
    return nama_lengkap;
  }

  public void setNama_lengkap(String nama_lengkap) {
    this.nama_lengkap = nama_lengkap;
  }

  public String getTempat_lahir() {
    return tempat_lahir;
  }

  public void setTempat_lahir(String tempat_lahir) {
    this.tempat_lahir = tempat_lahir;
  }

  public String getTanggal_lahir() {
    return tanggal_lahir;
  }

  public void setTanggal_lahir(String tanggal_lahir) {
    this.tanggal_lahir = tanggal_lahir;
  }

  public String getAlamat() {
    return alamat;
  }

  public void setAlamat(String alamat) {
    this.alamat = alamat;
  }

  public String getNomor_hp() {
    return nomor_hp;
  }

  public void setNomor_hp(String nomor_hp) {
    this.nomor_hp = nomor_hp;
  }

  public String getGender() {
    return gender;
  }

  public void setGender(String gender) {
    this.gender = gender;
  }

  public String getNama_jurusan() {
    return nama_jurusan;
  }

  public void setNama_jurusan(String nama_jurusan) {
    this.nama_jurusan = nama_jurusan;
  }
}
